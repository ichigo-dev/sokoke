/// # Sokoke Path
//------------------------------------------------------------------------------

use regex::Regex;


//------------------------------------------------------------------------------
/// Path
//------------------------------------------------------------------------------
#[derive(Debug)]
pub struct Path
{
    matcher: Regex,
}

impl Path
{
    //--------------------------------------------------------------------------
    /// Creates a new path.
    //--------------------------------------------------------------------------
    pub fn new(path: &str) -> Self
    {
        let mut regex = "^".to_string();
        regex.push_str(path);
        regex.push_str("$");
        Path
        {
            matcher: Regex::new(&regex).unwrap(),
        }
    }
}
